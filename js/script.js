function makanan1()
{
	var makan = document.getElementById('meno1').value;
	if(makan == "")
	{
		document.getElementById('meno1').value = "Meno Kucin Haro";
	}
	else
	{
		alert ("apakah anda tidak ingin makanan ini");
		document.getElementById('meno').value = "";
		document.getElementById('meno1').value = "";
		document.getElementById('1').value = "";
	}
}

